importClass(org.maxgamer.rs.model.interfaces.impl.dialogue.SpeechDialogue);

module.exports = {
    talkTo: function(player, npc){
        chat(npc, "Dooooom!");
        chat(player, "Where?");
        chat(npc, "All around us! I can feel it in the air, hear it on the wind, smell it...also in the air!", SpeechDialogue.WORRIED);
        chat(player, "Is there anything we can do about this doom?");
        chat(npc, "There is nothing you need to do my friend! I am the Doomsayer, although my real title could be something like the Danger Tutor.");
        chat(player, "Danger Tutor?", SpeechDialogue.CONFUSED);
        chat(npc, "Yes! I roam the world sensing danger.", SpeechDialogue.WORRIED);
        chat(npc, "If I find a dangerous area, then I put up warning signs that will tell you what is so dangerous about that area.");
        chat(npc, "If you see the signs often enough, then you can turn them off; by that time you likely know what the area has in store for you.");
        chat(player, "But what if I want to see the warnings again?");
        chat(npc, "That's why I'm waiting here!", SpeechDialogue.WORRIED);
        chat(npc, "If you want to see the warning messages again, I can turn them back on for you.");
        chat(player, "Thanks, I'll remember that if I see any warning messages.");
        chat(npc, "You're welcome!");
    }
}