importClass(org.maxgamer.rs.model.interfaces.impl.dialogue.SpeechDialogue);

module.exports = {
    talkTo: function(player, npc){
        chat(player, "Good day.");
        chat(npc, "To you too! I'm sorry but I'm guarding the castle. I'm not allowed to talk.");
        chat(player, "Nonsense, you're the Sergeant!");
        chat(npc, "That's right, but we've a rule that the guard who is patrolling outside the castle, isn't allowed to talk. Including me.");
        chat(npc, "Talk to my fellow object oriented guardsman: Guardsman Peale.");
        chat(npc, "He can give you the information you need and now leave me alone!", SpeechDialogue.MEAN_FACE);
    }
}